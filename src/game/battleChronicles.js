export const REGISTERS = ['memoir', 'saga', 'gazette'];

function reg(memoir, saga, gazette) {
  return { memoir, saga, gazette: gazette !== undefined ? gazette : saga };
}

// Один и тот же простой текст на все 3 регистра — используется там, где триплет ещё не
// написан (см. ограничение №1 выше).
function flat(text) {
  return { memoir: text, saga: text, gazette: text };
}

