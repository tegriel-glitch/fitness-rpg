function pickText(fieldOrFn, register) {
  const obj = typeof fieldOrFn === 'function' ? fieldOrFn : fieldOrFn;
  return obj[register] || obj.memoir;
}

function buildGenericFinisher(participants, register) {
  // Честный фолбэк для сценариев составов, для которых конкретный финишер ещё не написан
  // (двое "support"/"neutral", или отряд вообще без такой группы) — просто перечисляет вклад
  // каждого участника нейтральной связкой, без придуманного "как будто уникального" текста.
  const names = participants.map((p) => p.name);
  if (register === 'gazette') {
    return `Финальный удар стал результатом слаженных действий всех троих участников: ${names.join(', ')}.`;
  }
  return `${names.join(', ')} — трое, каждый по-своему, довели бой до конца, и ни один удар в отдельности не решил бы исход без остальных двух.`;
}

function pickFinisherEntry(entry) {
  // Некоторые ключи в finishers хранят массив из нескольких готовых вариантов текста
  // (переиспользуем контент, который случайно совпал по классификации, вместо того чтобы
  // выбрасывать один из двух написанных вариантов) — выбираем случайный.
  if (Array.isArray(entry)) return entry[Math.floor(Math.random() * entry.length)];
  return entry;
}

/**
 * Собирает готовый текст "хроники битвы" под конкретный состав отряда, исход и
 * зафиксированный при закрытии рейда регистр (memoir/saga/gazette).
 */
export function composeBattleChronicle({ bossId, participants, result }) {
  const isVictory = result === 'victory';
  // Регистры "сказание"/"дозорный листок" временно убраны (см. шапку файла) — всегда мемуары.
  const reg3 = 'memoir';

  // Приоритет №1: готовая цельная история под конкретный состав (сейчас — только Вепрь,
  // только победа). Работает только если classId известен для всех троих — иначе (старые
  // архивные записи без сохранённых классов) тихо проваливаемся в модульную сборку ниже,
  // а не показываем сломанный/пустой текст.
  if (bossId === 'boar' && isVictory) {
    const classIds = participants.map((p) => p.classId);
    if (classIds.every(Boolean)) {
      const key = classIds.slice().sort().join('|');
      const story = BOAR_VICTORY_STORIES[key];
      if (story) return `${BOAR_INTRO} ${story.text}`;
    }
  }

  const content = BOSS_CONTENT[bossId];

  if (!content) {
    // Боссы вне новой системы (эпические/мифические — ждут своей очереди) — generic-сборка.
    const lines = [
      pickText(GENERIC_FRAME.intro, reg3), pickText(GENERIC_FRAME.attack, reg3),
      ...participants.map((p, i) => {
        const variants = isVictory ? GENERIC_ROLE_VICTORY : GENERIC_ROLE_DEFEAT;
        return pickText(variants[i % variants.length](p.name), reg3);
      }),
      pickText(isVictory ? GENERIC_FRAME.victoryClose : GENERIC_FRAME.defeatClose, reg3),
    ];
    return lines.join(' ');
  }

  const axis = content.functionAxis;
  const roleBank = isVictory ? content.roles.victory : content.roles.defeat;

  const lines = [pickText(content.frame.intro, reg3), pickText(content.frame.attack, reg3)];

  const seenCount = {};
  participants.forEach((p) => {
    const variants = roleBank[p.classId] || GENERIC_ROLE_VICTORY;
    const occurrence = seenCount[p.classId || '__none__'] || 0;
    seenCount[p.classId || '__none__'] = occurrence + 1;
    const variantFn = variants[occurrence % variants.length];
    lines.push(pickText(variantFn(p.name), reg3));
  });

  if (isVictory) {
    // Порядок групп фиксируется по порядку их первого появления в оси класса этого босса —
    // группы называются просто A/B/C (первая/вторая/третья по оси), поэтому ключ финишера
    // ("A2B1" = группа A удвоена, группа B — соло-участник, группа C отсутствует) работает
    // одинаково для любой оси, будь то heavy/precise/support или analytic/instinctive/neutral.
    const groupLetters = [];
    Object.values(axis).forEach((g) => { if (!groupLetters.includes(g)) groupLetters.push(g); });
    const LETTER = { [groupLetters[0]]: 'A', [groupLetters[1]]: 'B', [groupLetters[2]]: 'C' };

    const byGroup = {};
    groupLetters.forEach((g) => { byGroup[g] = []; });
    participants.forEach((p) => {
      const g = axis[p.classId];
      if (g && byGroup[g]) byGroup[g].push(p.name);
    });
    const counts = groupLetters.map((g) => byGroup[g].length); // e.g. [1,1,1] or [2,1,0] etc.
    const distinctGroupsPresent = groupLetters.filter((g) => byGroup[g].length > 0);

    let finisherKey = null;
    let orderedNames = participants.map((p) => p.name);
    let mixedLookup = null; // set when we need content.finishers.threeSameMixed[group_majorityClass]

    if (distinctGroupsPresent.length === 3) {
      finisherKey = 'allThree';
      orderedNames = groupLetters.map((g) => byGroup[g][0]);
    } else if (distinctGroupsPresent.length === 1) {
      const group = distinctGroupsPresent[0];
      const groupParticipants = participants.filter((p) => axis[p.classId] === group);
      const uniqueClassIds = new Set(groupParticipants.map((p) => p.classId));
      if (uniqueClassIds.size === 1) {
        // Literally three of the same class — the original threeSame content applies as-is.
        finisherKey = 'threeSame';
        orderedNames = byGroup[group];
      } else {
        // Same function group, but a mix of the two classes that share it (e.g. 2 pathfinder
        // + 1 archmage, both "precise") — pick the write-up keyed by whichever class is in
        // the majority, so the text actually reflects who's really doing what.
        const classCounts = {};
        groupParticipants.forEach((p) => { classCounts[p.classId] = (classCounts[p.classId] || 0) + 1; });
        const majorityClassId = Object.keys(classCounts).find((c) => classCounts[c] === 2) || groupParticipants[0].classId;
        mixedLookup = `${group}_${majorityClassId}`;
        // Order: the two majority-class players first, the odd one out last — matches how
        // these texts were written ({n1}/{n2} = majority pair, {n3} = the other class).
        const majorityNames = groupParticipants.filter((p) => p.classId === majorityClassId).map((p) => p.name);
        const otherNames = groupParticipants.filter((p) => p.classId !== majorityClassId).map((p) => p.name);
        orderedNames = [...majorityNames, ...otherNames];
      }
    } else if (distinctGroupsPresent.length === 2) {
      const dupGroup = distinctGroupsPresent.find((g) => byGroup[g].length === 2);
      const soloGroup = distinctGroupsPresent.find((g) => byGroup[g].length === 1);
      finisherKey = `${LETTER[dupGroup]}2${LETTER[soloGroup]}1`;
      orderedNames = [...byGroup[dupGroup], byGroup[soloGroup][0]];
    }

    const bank = content.finishers;
    const finisherEntry = mixedLookup ? bank.threeSameMixed?.[mixedLookup] : (finisherKey ? bank[finisherKey] : null);
    if (finisherEntry && orderedNames.every(Boolean) && orderedNames.length === 3) {
      let text = pickText(pickFinisherEntry(finisherEntry), reg3);
      orderedNames.forEach((name, i) => { text = text.replaceAll(`{n${i + 1}}`, name); });
      lines.push(text);
    } else {
      lines.push(buildGenericFinisher(participants, reg3));
    }
  }

  lines.push(pickText(isVictory ? content.frame.victoryClose : content.frame.defeatClose, reg3));

  return lines.join(' ');
}
